using System;

namespace NewApp
{
    class Program
    {
        static void Main(string[] args)
        {
            // Doing math using the Math class
            Console.WriteLine(Math.Max(15, 30));
            Console.WriteLine(Math.Min(15, 30));
            Console.WriteLine(Math.Sqrt(100)); // 10
            Console.WriteLine(Math.Abs(-21.5));
            Console.WriteLine(Math.Round(29.99));
            Console.WriteLine(Math.Pow(2, 2));
        }
    }
}